    private int getNumero() {
        Niveau n = (Niveau) this.jCBNiveau.getSelectedItem();
        int num = 1;
        
        for(Classe c : ce.getEtablissement().getClasses()) {
            if (c.getNiveau().compareTo(n) == 0)
                num++;
        }
        
        return num;
    }

